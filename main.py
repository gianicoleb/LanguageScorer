language = input(
    "Which language are you learning? (french/english/german/spanish):").strip(
    ).lower()

# Select the corresponding dictionary
if language.lower() == "french":
  words = french
elif language.lower() == "english":
  words = english
elif language.lower() == "german":
  words = german
elif language.lower() == "spanish":
  words = spanish
else:
  print("Invalid language selection.")
  words = {}
incorrect = []
correctcount = 0
count = 0
score = 0
answers = []
# List to track answers ('y' or 'n')

i = 0  # Start index

while i < len(words):
  spanish_word, english = list(words.items())[i]

  response = input(
      f"Word {i + 1}: Do you know this word: {spanish_word}? (y/n/b for back): "
  ).strip().lower()

  if response == 'y':
    correctcount += 1
    answers.append('y')
    print(english)
    i += 1  # Move to the next word
  elif response == 'n':
    incorrect.append(spanish_word)
    answers.append('n')
    print(english)
    i += 1  # Move to the next word
  elif response == 'b' and i > 0:
    # Move back one word
    i -= 1
    last_answer = answers.pop()  # Remove the last answer from the list

    if last_answer == 'n' and incorrect:
      incorrect.pop()  # Remove the last word from incorrect if it was 'n'
    elif last_answer == 'y':
      correctcount -= 1  # Only subtract from correctcount if the last answer was 'y'

    count -= 2  # Adjust count to reflect going back
  else:
    print("Invalid response, please enter 'y', 'n', or 'b' to go back.")
    continue  # Stay on the current word if the response is invalid

  count += 1
  score = correctcount / count * 100
  print(f"Current score: {score:.2f}%")

  # Check if score has reached 75%
  if count > 10 and score <= 75:
    print(f"COMPLETE. 75% at word {i + 1}")
    break  # Stop the loop when 75% is reached

print(f"Final score: {score:.2f}%")
print(f"Incorrect words: {incorrect}")
